# ---------------------------------------------------------------------------
# ACTIVE_GROUPING — переключатель активной стратегии
# Меняйте здесь для экспериментов. Имя пишется в логи автоматически.
# ---------------------------------------------------------------------------
ACTIVE_GROUPING: str = "hybrid"


def get_active_groups() -> dict[str, dict]:
    """Вернуть активную стратегию группировки."""
    if ACTIVE_GROUPING not in GROUPING_REGISTRY:
        raise ValueError(
            f"Стратегия '{ACTIVE_GROUPING}' не найдена в GROUPING_REGISTRY. "
            f"Доступные: {list(GROUPING_REGISTRY.keys())}"
        )
    return GROUPING_REGISTRY[ACTIVE_GROUPING]


def get_grouping_meta() -> dict:
    """Вернуть метаданные активной стратегии для логирования."""
    groups = get_active_groups()
    return {
        "strategy_name": ACTIVE_GROUPING,
        "num_groups": len(groups),
        "groups": {
            gid: {
                "name": g["name"],
                "num_fields": len(g["fields"]),
                "fields": g["fields"],
            }
            for gid, g in groups.items()
        },
    }