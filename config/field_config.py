def get_fields_by_block(block: int) -> list[str]:
    """Вернуть все ID полей для заданного блока."""
    return [fid for fid, cfg in FIELD_CONFIG.items() if cfg["block"] == block]


def get_fields_by_sub_block(sub_block: int) -> list[str]:
    """Вернуть все ID полей для заданного подблока."""
    return [fid for fid, cfg in FIELD_CONFIG.items() if cfg["sub_block"] == sub_block]


def get_conditional_fields() -> list[str]:
    """Вернуть все поля, которые заполняются только при выполнении условия."""
    return [fid for fid, cfg in FIELD_CONFIG.items() if cfg["condition"] is not None]